    {
        //

        $validated = $request->validate([
            'name' => 'required|unique:departments',
            'code' => 'required|unique:departments|max:6'
        ]);


        $department = Department::create($validated);
        if($department){
            return response()->with(['message'=>'success'])->back();
        }else{
            return response()->withError(['message'=>'success'])->back();
        }

    }

